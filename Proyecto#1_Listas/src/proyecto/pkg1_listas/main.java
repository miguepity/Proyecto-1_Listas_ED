    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double promedio = 0;
        Random rnd = new Random();
        random = rnd.nextInt(student.getSize());

        txt_cuenta.setText(student.get(random).getNumero_cuenta());
        txt_nombre.setText(student.get(random).getNombre());
        txt_carrera.setText(student.get(random).getCarrera());

        for (int i = 0; i < student.get(random).getNotas().getSize(); i++) {
            promedio += student.get(random).getNotas().get(i);
        }

        txt_promedio.setText(Double.toString(promedio / student.get(random).getNotas().getSize()));
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        double promedio2 = 0;
        try {
            JFileChooser file = new JFileChooser();
            file.showSaveDialog(this);
            File guarda = file.getSelectedFile();
            BufferedWriter bw = null;
            if (guarda != null) {
                FileWriter save = new FileWriter(guarda+".csv");
                bw = new BufferedWriter(save);
                for (int i = 0; i < student.getSize(); i++) {
                    bw.write(student.get(i).getNumero_cuenta());
                    bw.write(",");
                    for (int j = 0; j < student.get(i).getNotas().getSize(); j++) {
                        promedio2 += student.get(i).getNotas().get(j);
                    }
                    promedio2 = promedio2 / student.get(i).getNotas().getSize();
                    bw.write(Double.toString(promedio2));
                    promedio2 = 0;
                    bw.write(",");
                    bw.newLine();
                }
                bw.close();
                JOptionPane.showMessageDialog(null,
                        "El archivo se a guardado Exitosamente",
                        "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,
                    "Su archivo no se ha guardado",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

